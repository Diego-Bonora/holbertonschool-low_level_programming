#include "3-calc.h"

/**
 * op_add - sums a and b
 * @a: input number
 * @b: input number
 * Return: integer a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract a and b
 * @a: input number
 * @b: input number
 * Return: integer a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplicates a and b
 * @a: input number
 * @b: input number
 * Return: integer a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides a and b
 * @a: input number
 * @b: input number
 * Return: integer a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module of a and b
 * @a: input number
 * @b: input number
 * Return: integer a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
